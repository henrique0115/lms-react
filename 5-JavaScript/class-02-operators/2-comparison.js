// Comparison Operators (Boolean)
// >    >=
// <    <=
// ==   !=
// ===  !==

let num1 = 2;
let num2 = 3;

console.log(num1 > num2);
console.log(num1 < num2);
console.log(num1 >= 2);
console.log(num2 <= 3);
console.log(num1 == num2);
console.log(num1 != num2);
console.log(num1 == "2");
console.log(num1 === "2");
console.log(num1 != "2");
console.log(num1 !== "2");
